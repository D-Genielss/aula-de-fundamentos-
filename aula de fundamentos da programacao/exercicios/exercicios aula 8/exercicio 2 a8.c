/* Faça um algoritmo que exiba todos números de 100 a 1 */

#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main(){

    //variaveis

    int contador = 100;

    //estrutura de repetição

    while(contador > 0){

        //mostrando contador
        printf("%d \n",contador);

        //diminuir
        contador --;

    }

    return 0;
}