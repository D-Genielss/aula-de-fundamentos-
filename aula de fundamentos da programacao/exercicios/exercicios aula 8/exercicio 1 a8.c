/* Faça um algoritmo que exiba todos números de 1 a 100. */

#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main(){

    //variaveis

    int contador = 1;

    //estrutura de repetição

    while(contador < 101){

        //mostrando contador
        printf("%d \n",contador);

        contador ++;

    }

    return 0;
}