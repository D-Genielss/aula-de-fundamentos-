/* Faça um algoritmo que exiba todos números de 100 a 1 */

#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main(){

    //variaveis

    int contador;

    //estrutura de repetição

    for(contador = 100 ; contador > -1 ;contador --){

        printf("%d \n",contador);
    
    }

    return 0;

}
