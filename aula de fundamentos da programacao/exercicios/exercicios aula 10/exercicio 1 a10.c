/* Faça um algoritmo que exiba todos números de 1 a 100. */

#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main(){

    //variaveis

    int contador;

    //estrutura de repetição

    for(contador = 0 ; contador < 101 ;contador ++){

        printf("%d \n",contador);
    
    }

    return 0;

}